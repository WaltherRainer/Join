function iconSvg({
  width = 20,
  height = 20,
  viewBox = "0 0 20 20",
  // ALT:
  paths = null,
  color = "currentColor",
  // NEU:
  content = "",
  defs = "",
} = {}) {
  // Wenn keine "content"-Struktur angegeben ist, aber "paths" vorhanden sind -> baue content daraus
  const autoContent =
    Array.isArray(paths) && paths.length
      ? paths
          .map(
            (p) =>
              `<path d="${p.d}" ${p.fill ? `fill="${p.fill}"` : `fill="${color}"`}></path>`
          )
          .join("")
      : "";

  return `
    <svg width="${width}" height="${height}" viewBox="${viewBox}" fill="none" xmlns="http://www.w3.org/2000/svg">
      ${defs ? `<defs>${defs}</defs>` : ""}
      ${content || autoContent}
    </svg>
  `;
}

